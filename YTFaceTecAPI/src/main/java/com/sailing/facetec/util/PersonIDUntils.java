    public static PersonIDEntity getPersonInfo(String name,String id){
        PersonIDEntity result = new PersonIDEntity();

        // 获取区域编码
        String areaCode = id.substring(0,6);
        // 省份
        String province = "";
        // 城市
        String city = "";
        // 行政区
        String district = areaCode;
        // 生日
        String birthday = id.substring(6,14);
        // 性别
        String gender = "0".equals(id.substring(16,17))?"女":"男";

        // 判断是否是直辖市
        if("0".equals(areaCode.substring(2,3))){
            // 直辖市 省市相同
            province = String.format("%s0000",areaCode.substring(0,2));
            city = province;
        }else{
            province = String.format("%s0000",areaCode.substring(0,2));
            city =  String.format("%s00",areaCode.substring(0,4));
        }

        result.setId(id);
        result.setName(name);
        result.setProvince(regionMap.get(Integer.parseInt(province)));
        result.setCity(regionMap.get(Integer.parseInt(city)));
        result.setDistrict(regionMap.get(Integer.parseInt(district)));
        result.setBirthDay(String.format("%s-%s-%s",birthday.substring(0,4),birthday.substring(4,6),birthday.substring(6)));
        result.setGender(gender);
        // 计算年龄
        result.setAge(
                Integer.parseInt(CommUtils.getCurrentDate().substring(0,4))
                        -Integer.parseInt(birthday.substring(0,4))
        );
        return result;
    }
